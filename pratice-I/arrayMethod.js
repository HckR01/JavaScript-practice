//push pop

const hobbi= ['reading','song']
console.log(hobbi)

hobbi.push('coding')
console.log(hobbi)

//adding at first 
hobbi.unshift('singing')
console.log(hobbi)

//removing from last    
hobbi.pop()
console.log(hobbi)
//removing from first
hobbi.shift()
console.log(hobbi)

//replace array element through index
hobbi[1]='dancing'
console.log(hobbi)