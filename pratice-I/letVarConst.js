// here we can discuss about let, var and const
// const use for constant values
// var use for global scope
// let use for block scope 
// but in general we use let and const

const id =1234;
let name = "John"; // block scope
var age = 30; // global scope

var age=34; // reassigning var is allowed
name = "Doe"; // reassigning let is allowed

console.log("ID:", id);
console.log("Name:", name);
console.log("Age:", age);


//var is not use coz it has global scope it can be change everywhere so we can use let
